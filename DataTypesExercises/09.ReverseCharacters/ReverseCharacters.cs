using System;

namespace _09.ReverseCharacters
{
    class ReverseCharacters
    {
        static void Main()
        {
            char letterOne = char.Parse(Console.ReadLine());
            char letterTwo = char.Parse(Console.ReadLine());
            char letterTree = char.Parse(Console.ReadLine());

            Console.Write(letterTree);
            Console.Write(letterTwo);
            Console.Write(letterOne);
            Console.WriteLine();
        }
    }
}
